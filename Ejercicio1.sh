Ejercicio nùmero one.
1:Whoami : Para imprimir el nombre del usuario efectivo.
2:Grep: Puede buscar palabras o un patròn, se imprimiran las lineas que lo contengan.
3.Less :Se usa para mostrar texto en la terminal y el contenido de un fichero linea y linea.
4.More : Permite mostrar el resultado de una ejecuciòn, se utiliza cuando un comando genera un gran desplazamiento.
5.Uname: Sirve para imprimir informaciòn de linux.
6.Top: Muestra el estado de el sistema, muestra un listado de los procesos que se estan llevando acabo.
7.Du: Se usa para estimar el estado del disco duro de un archivo.
8.Df:Despliega la informacion del espacio total,ocupado, libre de un sistema.
9.Ps:Listar y Manejar procesos.
10.Kill:Envia una señal de terminaciòn.
11.Find: Nos facilida la forma de encontrar un archivo.
12.Chmod:Se usa para cambiar el permiso de archivos y directorios.
13.Ssh:Permite accender de un ordenador a otro a traves de la red y mover ficheros entre dos maquinas.
14.Scp:Permite realizar la transferencia de archivos.
15.Uptme:Da la informaciòn de la hora actual,cuanto tiempo lleva en marcha el sistema.
16.W:Muestra la informacion de los usurios que se encuntran conectados en el sistema.
17. Wget:Descargador de archivos y sitios web.
18. Mount:Se utiliza para montar dispositivos y particiones para el uso de sistema operativo.
19. Tree:Lista de formna recursiva los directorios y archivos contenidos en una
